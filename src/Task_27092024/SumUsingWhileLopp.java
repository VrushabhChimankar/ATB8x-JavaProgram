package Task_27092024;

public class SumUsingWhileLopp {
    public static void main(String[] args) {

        //Write a Java program that calculates the sum of numbers from 1 to 100 using a while loop.

        int i = 0;
        int number = 1;
        while (number<=100){

            i+= number;
            number++;
            System.out.println("Sum of numbers from 1 to 100 is: " + i);

        }
    }

    }

