package sept_ex_19092024;

public class Lab_025_PreIncrementOperator {

    public static void main(String[] args) {

        byte a = 10;
        int result = ++a;
        System.out.println(a);
        System.out.println(result);

        //Line no | Exp | a
        // 7 |      | 10
        // 8 | result 11 | 11
        // 9 | result 11 | 11
    }
}
