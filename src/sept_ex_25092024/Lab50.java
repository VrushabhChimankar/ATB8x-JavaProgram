package sept_ex_25092024;

public class Lab50 {
    public static void main(String[] args) {


        //It will print upto y, because y comes before z and condition is less than z
        for (char c = 'a'; c < 'z'; c++) {

            System.out.println(c);
        }

        for (int i = 0; i > 0 ; i--) {
            System.out.println(i);
        }

    }
}
